  <h1 class="titulo" >Reseña Historica</h1>
  <p>La institución educativa San Isidro es producto de la reorganización impuesta por la ley 715 del 2001, y por ello la Resolución No. 1360 del 19 de noviembre de 2002 de la Secretaría de Educación y Cultura del Tolima, “Resuelve Art. 1° Integrar a la Institución Educativa San Isidro, en el Municipio de Ibagué los siguientes establecimientos educativos: Centro educativo San Isidro, Escuela urbana Batallón Jaime Rooke, y Escuela Urbana mixta Granada, educación Preescolar, Básica primaria y Básica secundaria de 6° a 8°. La última reorganización de las sedes ocurrió por cuenta de la Secretaría Municipal mediante Resolución No.71-00010 del 7 de enero de 2010.
  <p><br/> Resolución No. 293 del 7 de diciembre de 2.004 de la Secretaría de Educación del municipio de Ibagué, resuelve art. 1° reconocer los estudios en el nivel preescolar, grado transición nivel de básica primaria, grados de primero a quinto, Nivel de básica ciclo Secundaria, grados de sexto a noveno, y el grado décimo del nivel de Educación Media se reconoce hasta el 2005 de la Institución Educativa San Isidro.
  <p><br/>Mediante Resolución 281 del 10 de agosto de 2005 Secretaria de Educación del municipio de Ibagué, resuelve art. 1° reconocer/o aprobar los estudios de los niveles de Preescolar, grado transición, básica ciclo primaria, grados 1° a 5°, básica ciclo secundaria grados 6° a 9° y el grado 10° y 11° de la Educación Media Académica de la Institución Educativa San Isidro. Art. 2° autorizar la I. E. San Isidro, para expedir los títulos y certificados de conformidad con el decreto 1860 de 1994. Posteriormente, mediante resolución 2549 del 29 noviembre de 2011 se reconocen los avances del PEI hasta el año 2012. En el año 2013 los avances que ha tenido la INSTITUCION EDUCATIVA SAN ISIDRO, se reconocen mediante la resolución 71002635 del 3 de octubre de 2013, y por ello los estudios de pre escolar, primaria, básica y Media quedaron aprobados hasta el año 2.016.
  <p><br/>Actualmente los avances que ha tenido la institución Educativa Sn Isidro fueron estudiados por los supervisores de la oficina de inspección y Vigilancia en cabeza de los directores de núcleo HENRY CARDONA GARZON y LEONEL TORRES LOMBANA, y por eso la Secretaria de Educaciòn de Ibaguè “RECONOCE LOS AVANCES DEL PEI POR EL TERMINO DE CINCO (5) AÑOS DESDE EL 2017 HASTA EL 2021 INCLUSIVE, PARA LOS NIVELES PREESCOLAR, BASICA Y MEDIA ACADEMICA” mediante resolución No. 002846 del 23 de octubre de 2017.

